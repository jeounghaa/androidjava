package 클래스만들기;

class TV {
	// 함수정의 -> 메서드(method, 방법을 정의)
	public void on() {
		System.out.println("TV를 켜다.");
		System.out.println("코드를 끼다.");
	}
	public void off() {
		System.out.println("TV를 끄다.");
		System.out.println("코드를 뽑다");
	}
	public void channel() {
		System.out.println("채널을 변경하다.");
	}
	public void show() {
		System.out.println("화면을 보여주다");
	}
}
